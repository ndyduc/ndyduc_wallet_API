def extract_tokens():
    url = 'https://api.kraken.com/0/public/Ticker'
    response = requests.get(url)  # Gửi yêu cầu GET đến API
    response.raise_for_status()  # Ném một ngoại lệ nếu có lỗi trong phản hồi

    json_data = response.json()
    tokens = [token for token in json_data['result'].keys() if token.endswith("USD")]
    tokens_json = {"tokens": tokens}
    return json.dumps(tokens_json)

